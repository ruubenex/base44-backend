export const supportedLanguages = ['pt', 'en', 'fr', 'es', 'it', 'de'];

export const defaultLanguage = 'pt';

export function detectBrowserLanguage() {
  const browserLang = navigator.language || navigator.languages?.[0] || 'pt';
  const langCode = browserLang.split('-')[0].toLowerCase();
  return supportedLanguages.includes(langCode) ? langCode : defaultLanguage;
}
